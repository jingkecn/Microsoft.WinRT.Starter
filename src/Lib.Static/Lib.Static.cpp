double MyLib::MyStruct::add(double x, double y)
{
    return x + y;
}

double MyLib::MyStruct::substract(double x, double y)
{
    return x - y;
}

double MyLib::MyStruct::multiply(double x, double y)
{
    return x * y;
}

double MyLib::MyStruct::divide(double x, double y)
{
    return x / y;
}
